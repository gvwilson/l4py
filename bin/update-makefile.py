"""Scan lesson index.md files for %inc .out references and write dependencies.mk."""

import re
from pathlib import Path

SKIP = {
    "docs",
    "bibliography",
    "glossary",
    "intro",
    "finale",
    "_extras",
    "_static",
    "_templates",
}


def find_out_refs(root):
    """Return sorted list of <dir>/<file>.out paths referenced via %inc."""
    refs = []
    for index_md in sorted(root.glob("*/index.md")):
        lesson_dir = index_md.parent.name
        if lesson_dir in SKIP:
            continue
        for m in re.finditer(r"\[%inc\s+([^\s%]*\.out)", index_md.read_text()):
            refs.append(f"{lesson_dir}/{m.group(1)}")
    return refs


def main():
    root = Path(".")
    refs = find_out_refs(root)
    with open("dependencies.mk", "w") as f:
        f.write("# Auto-generated by bin/update-makefile.py\n")
        f.write(f"OUTPUTS = {' '.join(refs)}\n")
    print(f"Wrote dependencies.mk with {len(refs)} output files")


if __name__ == "__main__":
    main()
